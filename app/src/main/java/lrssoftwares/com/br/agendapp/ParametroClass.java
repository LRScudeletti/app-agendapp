package lrssoftwares.com.br.agendapp;

class ParametroClass {
    //region [ VARIAVEIS ]
    private int id;
    private String nome;
    private int valor;
    //endregion

    //region [ GETS E SETS ]
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    String getNome() {
        return nome;
    }

    void setNome(String nome) {
        this.nome = nome;
    }

    int getValor() {
        return valor;
    }

    void setValor(int valor) {
        this.valor = valor;
    }
    //endregion
}
